    private void getTicketBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getTicketBtnActionPerformed

         String name = name_field.getText();
         String symptom = symptom_field.getText();
        
        if (name.length() == 0 || symptom.length() == 0){
            out_field.setText("Please fill out all fields!");
        } else {
            name_field.setText("");
            symptom_field.setText("");
            
            if (queue.isEmpty()){
                out_field.setText( "Your are first in line! Be ready to get probed!! \n" );
                 queue.enqueue(name);
            }
            else if (queue.size() == 1){
                out_field.setText( "There is one patient ahead of you \n" );
                 queue.enqueue(name);
            } else {
                out_field.setText( "There are  " + queue.size() + " patients ahead of you \n" );
                queue.enqueue(name);
            }
            
        }
    }//GEN-LAST:event_getTicketBtnActionPerformed

